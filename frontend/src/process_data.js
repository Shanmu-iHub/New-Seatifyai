const lines = text.trim().split('\n');
const courseMap = {};

lines.forEach(line => {
  const parts = line.split('\t').map(p => p.trim());
  if (parts.length < 5) return;

  const [category, courseName, programName, collegeName, fee, totalSeats, seatsAvailable, status, emoji] = parts;

  if (status.toLowerCase() !== 'active') return;

  // Use (Category, Course Name) as the key to group programs
  const key = `${category}-${courseName}`;
  if (!courseMap[key]) {
    courseMap[key] = {
      name: courseName,
      collegeName: collegeName,
      type: programName, // We'll use the first program name as the 'type' for the course header
      category: category === 'k12' ? 'K-12' : category,
      emoji: emoji || '📚',
      programs: []
    };
  }

  courseMap[key].programs.push({
    name: programName,
    fee: Number(fee) || 0,
    seats: Number(seatsAvailable) || 0,
    totalSeats: Number(totalSeats) || 0
  });
});

const result = Object.values(courseMap);
